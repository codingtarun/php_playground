/*
|--------------------------------------------------------------------------
| PHP Operators 
|--------------------------------------------------------------------------
|
| Symbols that tells the PHP processor to perform specific task on provided
| variables or data.
| 
| There is 10 different types of Operator supported by PHP : 
| 1. Arithmetic Operators
| 2. Assignment Operators
| 3. Comparision Operatos
| 4. Increment/Decrement Operators
| 5. Logical Operators
| 6. String Operators
| 7. Array Operators
| 8. Bitwise Operators
| 9. Null Coalescing Operator
|
|
*/


/*
|--------------------------------------------------------------------------
| Arithmetic Operators
|--------------------------------------------------------------------------
|
| The arithmetic Operators are used to perform basic mathematical operations.
| 1. Addition (+) : Adds two operands.
| 2. Subtraction (-) : Subtract the second operand from first.
| 3. Multiplication (*) : Multiplies two operands
| 4. Division (/) : Divides  the first operand by the second.
| 5. Modulus (%) : Returns the remainder of the division.
| 6. Exponentiation (**) : Raises the first operand to the power of second.
| 
|
*/

/*
|--------------------------------------------------------------------------
| Assignment Operators
|--------------------------------------------------------------------------
|
| The assignment operators are used to write values to variables
| 1. Assignment (=) : Assign a value to variable
| 2. Addition Assignment ($a += $b) : Adds the right operand to left operand 
|                                     and saves it to left operand.
| 3. Multiplication Assignment ($a *= $b) : Multiply right operand with left
|                                           operand and saves in left operand.
| 4. Division Assignment ($a /= $b) : Divides right operand with the left 
|                                     operand and saves result in left operand.
| 5. Modulus assignment ($a %= $b): Takes the modulus using two operands and 
|                                   assigns the result to the left operand.
|
*/